public class StringExercise {
    public static void main(String[] args) {
String e1 = "We don't need no education \nWe don't need no thought control";
System.out.println(e1);

String e2 = "Check \"this\" out!, \"s inside of \"s!";
System.out.println(e2);

String e3 = "In windows, the main drive is usually C:\\";
System.out.println(e3);

String e4 = "I can do backslashes \\, double backslashes \\\\,\n" +
        "and the amazing triple backslash \\\\\\!";
System.out.println(e4);
    }
}
